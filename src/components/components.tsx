// 12. ChartCard - GPA Projection SVG Chart Panel
interface ChartCardProps {
  title: string;
  value: string | number;
  caption: string;
  chartData: { subject: string; score: number }[];
  className?: string;
}

export const ChartCard: React.FC<ChartCardProps> = ({
  title,
  value,
  caption,
  chartData,
  className = '',
}) => {
  const height = 120;
  const width = 450;
  const padding = 20;

  const max = 100;
  const min = 0;
  const range = max - min;

  const coords = chartData.map((d, i) => {
    const x = padding + (i / (chartData.length - 1)) * (width - 2 * padding);
    const y = height - padding - ((d.score - min) / range) * (height - 2 * padding);
    return { x, y, subject: d.subject, score: d.score };
  });

  const pathD = coords.map((c, i) => `${i === 0 ? 'M' : 'L'} ${c.x} ${c.y}`).join(' ');
  const areaD = coords.length > 0 
    ? `${pathD} L ${coords[coords.length - 1].x} ${height - padding} L ${coords[0].x} ${height - padding} Z`
    : '';

  return (
    <BentoCard size="none" className={`p-6 space-y-6 ${className}`}>
      <div className="flex items-start justify-between border-b border-line pb-4">
        <div>
          <span className="text-[10px] uppercase font-mono tracking-wider text-text-dim block">
            {title}
          </span>
          <div className="flex items-baseline space-x-2 mt-1">
            <Metric value={value} className="text-3xl font-bold font-mono" />
            <span className="text-[10px] text-text-dim font-mono">on 4.0 scale</span>
          </div>
          <span className="text-[10px] text-speak font-semibold uppercase block mt-1 font-mono">
            {caption}
          </span>
        </div>
        <div className="text-[9px] uppercase font-mono tracking-wider text-text-dim bg-surface-2 px-2.5 py-1 rounded border border-line">
          Forecast Active
        </div>
      </div>

      <div className="space-y-4">
        <div className="relative h-[130px] w-full border border-line bg-surface-2/50 rounded-lg p-2 overflow-hidden">
          <svg viewBox={`0 0 ${width} ${height}`} className="w-full h-full overflow-visible">
            <line x1={padding} y1={height/2} x2={width-padding} y2={height/2} stroke="var(--line)" strokeDasharray="3 3" />
            
            {areaD && (
              <path
                d={areaD}
                fill="url(#chart-gradient)"
                opacity="0.15"
              />
            )}

            {pathD && (
              <path
                d={pathD}
                fill="none"
                stroke="var(--color-decide)"
                strokeWidth="2.5"
                strokeLinecap="round"
                strokeLinejoin="round"
              />
            )}

            {coords.map((c, i) => (
              <g key={i}>
                <circle
                  cx={c.x}
                  cy={c.y}
                  r="4"
                  fill="white"
                  stroke="var(--color-decide)"
                  strokeWidth="2"
                />
                <text
                  x={c.x}
                  y={c.y - 8}
                  textAnchor="middle"
                  className="font-mono text-[9px] font-bold fill-text"
                >
                  {c.score}%
                </text>
              </g>
            ))}

            <defs>
              <linearGradient id="chart-gradient" x1="0" y1="0" x2="0" y2="1">
                <stop offset="0%" stopColor="var(--color-decide)" />
                <stop offset="100%" stopColor="var(--color-decide)" stopOpacity="0" />
              </linearGradient>
            </defs>
          </svg>
        </div>

        <div className="grid grid-cols-2 sm:grid-cols-4 gap-2 pt-2 border-t border-line">
          {coords.map((c, i) => (
            <div key={i} className="text-center font-mono text-[9px]">
              <span className="text-text-dim block truncate">{c.subject}</span>
              <span className="font-bold text-text mt-0.5 block">{c.score}%</span>
            </div>
          ))}
        </div>
      </div>
    </BentoCard>
  );
};

