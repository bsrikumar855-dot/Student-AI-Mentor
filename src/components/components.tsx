// 10. ListRow - dot + label + value + trend row
interface ListRowProps {
  dotColorClass: string;
  label: string;
  subLabel?: string;
  value: string | number;
  trend?: 'improving' | 'declining' | 'stable';
  onClick?: () => void;
  className?: string;
}

export const ListRow: React.FC<ListRowProps> = ({
  dotColorClass,
  label,
  subLabel,
  value,
  trend,
  onClick,
  className = '',
}) => {
  const renderTrendIcon = () => {
    if (!trend) return null;
    if (trend === 'improving') {
      return <ArrowUpRight className="w-3.5 h-3.5 text-guard shrink-0" />;
    }
    if (trend === 'declining') {
      return <ArrowDownRight className="w-3.5 h-3.5 text-risk-high shrink-0" />;
    }
    return <Minus className="w-3.5 h-3.5 text-text-dim shrink-0" />;
  };

  return (
    <div
      onClick={onClick}
      className={`flex items-center justify-between py-2.5 px-4 hover:bg-surface-2/50 transition-colors ${
        onClick ? 'cursor-pointer' : ''
      } ${className}`}
    >
      <div className="flex items-center space-x-3 min-w-0">
        <span className={`w-2 h-2 rounded-full ${dotColorClass} shrink-0`} />
        <div className="min-w-0">
          <p className="text-xs font-semibold text-text truncate">{label}</p>
          {subLabel && <p className="text-[10px] text-text-dim truncate">{subLabel}</p>}
        </div>
      </div>
      <div className="flex items-center space-x-2 text-right shrink-0">
        <Metric value={value} className="text-xs font-bold font-mono" />
        {renderTrendIcon()}
      </div>
    </div>
  );
};

