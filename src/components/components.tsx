// 8. Custom Toast Notification Context
interface ToastMessage {
  id: string;
  title: string;
  message: string;
  type?: 'decide' | 'speak' | 'guard' | 'error';
}

const ToastContext = React.createContext<{
  toast: (title: string, message: string, type?: 'decide' | 'speak' | 'guard' | 'error') => void;
} | null>(null);

export const useToast = () => {
  const context = React.useContext(ToastContext);
  if (!context) throw new Error('useToast must be used within ToastProvider');
  return context;
};

export const ToastProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [toasts, setToasts] = React.useState<ToastMessage[]>([]);

  const toast = React.useCallback(
    (title: string, message: string, type: 'decide' | 'speak' | 'guard' | 'error' = 'decide') => {
      const id = Math.random().toString(36).slice(2);
      setToasts((prev) => [...prev, { id, title, message, type }]);
      setTimeout(() => {
        setToasts((prev) => prev.filter((t) => t.id !== id));
      }, 4000);
    },
    []
  );

  return (
    <ToastContext.Provider value={{ toast }}>
      {children}
      {/* Toast container floating on screen */}
      <div className="fixed bottom-5 right-5 z-50 flex flex-col space-y-2 max-w-md w-full px-4 md:px-0">
        {toasts.map((t) => {
          let borderType = 'border-l-4 border-l-decide';
          if (t.type === 'speak') borderType = 'border-l-4 border-l-speak';
          if (t.type === 'guard') borderType = 'border-l-4 border-l-guard';
          if (t.type === 'error') borderType = 'border-l-4 border-l-risk-high';

          return (
            <div
              key={t.id}
              className={`p-4 rounded-[12px] shadow-lg bg-surface border border-line flex items-start space-x-3 transition-all duration-300 animate-slide-in ${borderType}`}
            >
              <div className="flex-1">
                <h5 className="text-xs font-bold uppercase tracking-wider text-text">{t.title}</h5>
                <p className="text-xs text-text-dim mt-1 leading-snug">{t.message}</p>
              </div>
              <button
                onClick={() => setToasts((prev) => prev.filter((item) => item.id !== t.id))}
                className="text-text-dim hover:text-text text-xs"
              >
                ×
              </button>
            </div>
          );
        })}
      </div>
    </ToastContext.Provider>
  );
};

